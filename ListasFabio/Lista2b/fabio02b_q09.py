"""
	Questão: Lista 2b 09
	Descrição: retorna o dia da semana
"""
def main():
    #entrada
    numero = int(input("digite um neumro de 1 a 7: "))
    #processamento
    if numero == 1 :
        print("Domingo")
    elif numero == 2 :
        print("Segunda")
    elif numero == 3 :
        print("Terça")
    elif numero == 4 :
        print("Quarta")
    elif numero == 5 :
        print("Quinta")
    elif numero == 6 :
        print("Sexta")
    elif numero == 7 :
        print("Sabado")
    #saida

if __name__ == '__main__':
    main()
